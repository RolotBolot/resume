--            данные в БД во время тестирования (verification на уровне данных).
--  СУБД: PostgreSQL / MySQL (синтаксис совместим для базовых конструкций)
-- ============================================================================
--
--  Учебная схема — интернет-магазин:
--
--    users(id, name, email, city, created_at)
--    orders(id, user_id, status, total, created_at)
--    products(id, title, price, category, in_stock)
--    order_items(id, order_id, product_id, qty, price)
--
--  status у заказа: 'new' | 'paid' | 'shipped' | 'cancelled'
-- ============================================================================


-- ----------------------------------------------------------------------------
-- 1. Базовая выборка. Проверяем, что пользователь вообще завёлся в системе
--    после регистрации (смотрим конкретного по email).
-- ----------------------------------------------------------------------------
SELECT id, name, email, city, created_at
FROM users
WHERE email = 'test_user@example.com';


-- ----------------------------------------------------------------------------
-- 2. Фильтр + сортировка + лимит. Последние 10 зарегистрированных —
--    удобно для проверки, что новые юзеры реально пишутся в базу.
-- ----------------------------------------------------------------------------
SELECT id, name, email, created_at
FROM users
ORDER BY created_at DESC
LIMIT 10;


-- ----------------------------------------------------------------------------
-- 3. Поиск по части значения (LIKE). Например, ищем тестовые аккаунты,
--    которые остались в базе после тестов и могут портить статистику.
-- ----------------------------------------------------------------------------
SELECT id, name, email
FROM users
WHERE email LIKE '%test%'
   OR name  LIKE 'QA%';


-- ----------------------------------------------------------------------------
-- 4. Несколько условий + диапазон. Заказы за период с суммой выше порога —
--    проверяем, что фильтры в отчёте на сайте совпадают с данными в БД.
-- ----------------------------------------------------------------------------
SELECT id, user_id, status, total, created_at
FROM orders
WHERE created_at >= '2026-01-01'
  AND created_at <  '2026-02-01'
  AND total > 1000
  AND status IN ('paid', 'shipped');


-- ----------------------------------------------------------------------------
-- 5. INNER JOIN. Соединяем заказы с их авторами — нужно, когда на UI
--    показывается имя клиента, и мы сверяем его с реальным владельцем заказа.
-- ----------------------------------------------------------------------------
SELECT o.id        AS order_id,
       u.name      AS customer,
       o.status,
       o.total
FROM orders o
INNER JOIN users u ON u.id = o.user_id
WHERE o.status = 'paid'
ORDER BY o.total DESC;


-- ----------------------------------------------------------------------------
-- 6. LEFT JOIN + поиск "сирот". Заказы, у которых НЕТ пользователя —
--    это баг целостности данных (заказ есть, а клиент удалён/потерян).
-- ----------------------------------------------------------------------------
SELECT o.id AS order_id, o.user_id, o.total
FROM orders o
LEFT JOIN users u ON u.id = o.user_id
WHERE u.id IS NULL;


-- ----------------------------------------------------------------------------
-- 7. Группировка + агрегаты. Сколько заказов и на какую сумму у каждого юзера —
--    сверяем с тем, что показывает личный кабинет.
-- ----------------------------------------------------------------------------
SELECT u.id,
       u.name,
       COUNT(o.id)      AS orders_count,
       COALESCE(SUM(o.total), 0) AS orders_sum,
       ROUND(AVG(o.total), 2)    AS avg_check
FROM users u
LEFT JOIN orders o ON o.user_id = u.id
GROUP BY u.id, u.name
ORDER BY orders_sum DESC;


-- ----------------------------------------------------------------------------
-- 8. GROUP BY + HAVING. Находим "подозрительных" клиентов с большим числом
--    заказов — полезно для проверки антифрода / лимитов.
-- ----------------------------------------------------------------------------
SELECT user_id, COUNT(*) AS cnt
FROM orders
GROUP BY user_id
HAVING COUNT(*) > 5
ORDER BY cnt DESC;


-- ----------------------------------------------------------------------------
-- 9. Поиск дублей. Один email не должен встречаться дважды —
--    классический баг регистрации. Если строки есть — баг подтверждён.
-- ----------------------------------------------------------------------------
SELECT email, COUNT(*) AS dup_count
FROM users
GROUP BY email
HAVING COUNT(*) > 1;


-- ----------------------------------------------------------------------------
-- 10. Проверка NULL / пустых обязательных полей. Так ловим заказы без суммы
--     или товары без цены — данные, которых быть не должно.
-- ----------------------------------------------------------------------------
SELECT id, title, price, category
FROM products
WHERE price IS NULL
   OR price <= 0
   OR title IS NULL
   OR title = '';


-- ----------------------------------------------------------------------------
-- 11. Подзапрос. Пользователи, которые ещё ни разу не делали заказ —
--     сегмент для проверки сценариев "новый клиент".
-- ----------------------------------------------------------------------------
SELECT id, name, email
FROM users
WHERE id NOT IN (SELECT DISTINCT user_id FROM orders WHERE user_id IS NOT NULL);


-- ----------------------------------------------------------------------------
-- 12. Сверка расчёта. Считаем сумму заказа из позиций (qty * price) и
--     сравниваем с total в заказе — ищем расхождения в подсчёте корзины.
-- ----------------------------------------------------------------------------
SELECT o.id AS order_id,
       o.total                          AS total_in_order,
       SUM(oi.qty * oi.price)           AS total_calculated,
       o.total - SUM(oi.qty * oi.price) AS difference
FROM orders o
JOIN order_items oi ON oi.order_id = o.id
GROUP BY o.id, o.total
HAVING o.total <> SUM(oi.qty * oi.price);   -- строки здесь = баг в расчёте


-- ----------------------------------------------------------------------------
-- 13. Распределение по статусам. Быстрый "снимок" данных перед тестированием
--     отчётов — сколько заказов в каждом статусе.
-- ----------------------------------------------------------------------------
SELECT status, COUNT(*) AS cnt
FROM orders
GROUP BY status
ORDER BY cnt DESC;

